package BOJ.PS시작하기.입출력;

import java.util.Scanner;

/**
 * 
 * boj_2742. 기찍 N
 * @author mihyun
 *
 */
public class 기찍N {

	public static void main(String[] args) {

		Scanner sc = new Scanner(System.in);
		
		int N =sc.nextInt();
		
		for (int i = N; i > 0; i--) {
			System.out.println(i);
		}
	}
}
